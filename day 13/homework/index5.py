#7) მომხმარებელს შემოატაინეთ 2 რიცხვი, და შემდეგ გამოიყენეთ შედარების ოპერატორები რა რიცხვსაც შეიყვანს მომხმარებელი, მასე გამოიყენთ შედარების ოპერატორები ==,!=,<,>,<=,=>.
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))
print(f"\nშედარების შედეგები:")
print(f"{num1} == {num2}: {num1 == num2}")  
print(f"{num1} != {num2}: {num1 != num2}")  
print(f"{num1} < {num2}: {num1 < num2}")    
print(f"{num1} > {num2}: {num1 > num2}")    
print(f"{num1} <= {num2}: {num1 <= num2}")  
print(f"{num1} >= {num2}: {num1 >= num2}")  
